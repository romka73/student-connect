        // Get saved language from localStorage or default to Russian
        let currentLanguage = localStorage.getItem('language') || 'ru';

        // Function to change language
        function changeLanguage(lang) {
            currentLanguage = lang;
            localStorage.setItem('language', lang);
            updateTexts();
            updateLanguageButton();
        }

        // Function to update all texts on the page
        function updateTexts() {
            const elements = document.querySelectorAll('[data-i18n]');
            elements.forEach(element => {
                const key = element.getAttribute('data-i18n');
                if (translations[currentLanguage] && translations[currentLanguage][key]) {
                    element.textContent = translations[currentLanguage][key];
                }
            });
        }

        // Function to update language button text
        function updateLanguageButton() {
            const languageTexts = {
                'ru': 'Рус',
                'kz': 'Қаз',
                'en': 'Eng'
            };
            
            document.getElementById('current-language').textContent = languageTexts[currentLanguage] || 'Рус';
            document.getElementById('mobile-current-language').textContent = languageTexts[currentLanguage] || 'Рус';
        }

        // Mobile Menu Toggle
        const menuToggle = document.getElementById('menu-toggle');
        const mobileMenu = document.getElementById('mobileMenu');
        
        menuToggle.addEventListener('click', () => {
            mobileMenu.classList.toggle('active');
        });
        
        // Close menu when clicking on a link
        const mobileLinks = mobileMenu.querySelectorAll('a');
        mobileLinks.forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.remove('active');
            });
        });

        // Close language dropdown when clicking outside
        document.addEventListener('click', function(event) {
            if (!event.target.closest('.language-selector')) {
                const dropdowns = document.querySelectorAll('.language-dropdown');
                dropdowns.forEach(dropdown => {
                    dropdown.style.display = 'none';
                });
            }
        });

        // Initialize the page with the selected language
        document.addEventListener('DOMContentLoaded', () => {
            updateTexts();
            updateLanguageButton();
        });
             
        